    public void preencherTabela() {
        CompraProdutoDAO dao = new CompraProdutoDAO();
        DefaultTableModel dtm = (DefaultTableModel)tblCadastrados.getModel();
        
        dtm.setRowCount(0);
        for(CompraProduto cp: dao.read(estaCompra)){
            dtm.addRow(new Object[]{
                cp.getProduto().getId(),
                cp.getProduto().getDescricao(),
                cp.getQuantidade(),
                cp.getValorUnitarioCompra(),
                cp.getValorTotal()
            });
        }
    }
    
    public void preencherComboProduto() {
        ProdutoDAO dao = new ProdutoDAO();
        
        for(Produto p : dao.read()) {
            cmbProduto.addItem(p);
        }
    }
    
    public void limpar() {
        esteProdCompra = null;
        txtQuantidade.setText("");
        cmbProduto.setSelectedIndex(0);
    }


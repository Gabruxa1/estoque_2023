    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblCadastrados.getModel();
        CompraDAO dao = new CompraDAO();
        
        
        dtm.setRowCount(0);
        for(Compra c : dao.read()) {
            dtm.addRow(new Object[] {
                c.getId(),
                new SimpleDateFormat("dd/MM/yyyy").format(c.getData()),
                new SimpleDateFormat("HH:mm:ss").format(c.getData()),
                c.getFornecedor(),
                c.getUsuario()
            });
        }
    }
    
    public void preencherComboFornecedor() {
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f : dao.read()) {
            cmbFornecedor.addItem(f);
        }
    }
    
    public void preencherComboUsuario() {
        UsuarioDAO dao = new UsuarioDAO();
        
        for(Usuario u : dao.read()) {
            cmbUsuario.addItem(u);
        }
    }
    
    public void limpar() {
        estaCompra = null;
        cmbFornecedor.setSelectedIndex(0);
        cmbUsuario.setSelectedIndex(0);
    }


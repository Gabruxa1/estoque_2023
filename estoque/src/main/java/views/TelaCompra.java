    private void tblCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastradosMouseClicked
        if (tblCadastrados.getSelectedRow() != -1) {
            CompraDAO dao = new CompraDAO();
            estaCompra = dao.find(Integer.parseInt(tblCadastrados.getValueAt(tblCadastrados.getSelectedRow(), 0).toString()));
            
            // Selecionar combo de Fornecedor
            for(int i=0; i<cmbFornecedor.getItemCount(); i++) {
                if(estaCompra.getFornecedor().getId() == cmbFornecedor.getItemAt(i).getId())
                    cmbFornecedor.setSelectedIndex(i);
            }
            
            // Seleconar combo de Usuario
            for(int i=0; i<cmbUsuario.getItemCount(); i++) {
                if(estaCompra.getUsuario().getId() == cmbUsuario.getItemAt(i).getId())
                    cmbUsuario.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tblCadastradosMouseClicked

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        estaCompra = new Compra();
        estaCompra.setFornecedor((Fornecedor)cmbFornecedor.getSelectedItem());
        estaCompra.setUsuario((Usuario)cmbUsuario.getSelectedItem());
        
        new CompraDAO().create(estaCompra);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnCadastrarActionPerformed

